#!/usr/bin/env python3

str_num1 = input("Give me the first number: ")
str_num2 = input("Give me the second number: ")
num1 = int(str_num1)
num2 = int(str_num2)

print("Thank you!")
print(f"{str_num1} + {str_num2} = {str(num1 + num2)}")
print(f"{str_num1} - {str_num2} = {str(num1 - num2)}")
print(f"{str_num1} / {str_num2} = {str(num1 // num2)}")
print(f"{str_num1} * {str_num2} = {str(num1 * num2)}")